package pepse.world;

/**
 * enum representing each avatar motion state
 */
public enum AvatarMotionState {
    /**
     * when the avatar is on the ground and not moving sideways.
     */
    IDLE,
    /**
     * when the avatar is moving sideways.
     */
    RUN,
    /**
     * when the avatar is in the air and not moving sideways.
     */
    JUMP}
